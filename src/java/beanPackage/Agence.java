package beanPackage;

/**
 *
 * @author CHEUKINHO
 */
public class Agence {
    
    private String institule_agence, rue, ville;
    private int id_agence, code_postale, numero, bte;

    public String getInstitule_agence() {
        return institule_agence;
    }

    public void setInstitule_agence(String institule_agence) {
        this.institule_agence = institule_agence;
    }

    public String getRue() {
        return rue;
    }

    public void setRue(String rue) {
        this.rue = rue;
    }

    public String getVille() {
        return ville;
    }

    public void setVille(String ville) {
        this.ville = ville;
    }

    public int getId_agence() {
        return id_agence;
    }

    public void setId_agence(int id_agence) {
        this.id_agence = id_agence;
    }

    public int getCode_postale() {
        return code_postale;
    }

    public void setCode_postale(int code_postale) {
        this.code_postale = code_postale;
    }

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

    public int getBte() {
        return bte;
    }

    public void setBte(int bte) {
        this.bte = bte;
    }
    
    
}
